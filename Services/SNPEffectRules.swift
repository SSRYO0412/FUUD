import Foundation

/// SNP影響の種類
enum SNPImpactType {
    case protective  // 保護因子
    case risk        // リスク因子
    case neutral     // 中立
}

/// SNP影響因子ルール
struct SNPEffectRule {
    let snpID: String
    let protectiveGenotypes: [String]  // 保護因子となる遺伝子型
    let riskGenotypes: [String]        // リスク因子となる遺伝子型

    /// 遺伝子型から影響タイプを判定
    func impact(for genotype: String) -> SNPImpactType {
        if protectiveGenotypes.contains(genotype) {
            return .protective
        } else if riskGenotypes.contains(genotype) {
            return .risk
        } else {
            return .neutral
        }
    }
}

