/// SNP影響因子カウント結果
struct SNPImpactCount {
    let protective: Int  // 保護因子数
    let risk: Int        // リスク因子数
    let neutral: Int     // 中立数

    /// 総SNP数
    var total: Int {
        return protective + risk + neutral
    }

    /// 保護因子の割合（0.0-1.0）
    var protectiveRatio: Double {
        guard total > 0 else { return 0.0 }
        return Double(protective) / Double(total)
    }

    /// リスク因子の割合（0.0-1.0）
    var riskRatio: Double {
        guard total > 0 else { return 0.0 }
        return Double(risk) / Double(total)
    }

    /// スコア（-100 to +100）
    /// 保護因子が多いほど高スコア、リスク因子が多いほど低スコア
    var score: Int {
        guard total > 0 else { return 0 }
        let netImpact = protective - risk
        let scoreRaw = (Double(netImpact) / Double(total)) * 100.0
        return Int(scoreRaw)
    }

    /// 5段階評価レベル
    enum ScoreLevel: String {
        case high = "高い"
        case slightlyHigh = "やや高い"
        case normal = "普通"
        case slightlyLow = "やや低い"
        case low = "低い"
    }

    /// 5段階評価を取得
    var scoreLevel: ScoreLevel {
        switch score {
        case 20...100:
            return .high
        case 1...19:
            return .slightlyHigh
        case -19...0:
            return .normal
        case -99...(-20):
            return .slightlyLow
        default:
            return .low
        }
    }
}
